// =========================================================================
// MAPEADOR: retorna o dicionário de candidatos pelo id do cargo e contexto
// =========================================================================
export function getCandidatos(cargoId, turno, tipoEleicao) {
  if (tipoEleicao === 'municipal') {
    if (cargoId === 'prefeito') return turno === 2 ? CANDIDATOS_PREFEITO_2T : CANDIDATOS_PREFEITO;
    if (cargoId === 'vereador') return CANDIDATOS_VEREADOR;
  } else {
    if (cargoId === 'presidente') return turno === 2 ? CANDIDATOS_PRESIDENTE_2T : CANDIDATOS_PRESIDENTE;
    if (cargoId === 'governador') return turno === 2 ? CANDIDATOS_GOVERNADOR_2T : CANDIDATOS_GOVERNADOR;
    if (cargoId === 'dep_federal')  return CANDIDATOS_DEP_FEDERAL;
    if (cargoId === 'dep_estadual') return CANDIDATOS_DEP_ESTADUAL;
    if (cargoId === 'senador_1' || cargoId === 'senador_2') return CANDIDATOS_SENADOR;
  }
  return {};
}